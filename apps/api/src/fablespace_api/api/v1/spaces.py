@router.post("/{space_id}/enter")
def enter_space(
    request: Request,
    background_tasks: BackgroundTasks,
    space_id: str,
    data: EnterSpaceRequest | None = None,
) -> dict[str, Any]:
    user_id = get_user_id(request)
    result = spaces_service(request).enter_space(
        space_id,
        password=data.password if data else "",
        user_id=user_id,
        visitor_gender=data.visitor_gender if data else "",
    )

    # Trigger notification for tavern owner
    if user_id:
        tavern = spaces_service(request).store.get_space(space_id)
        if tavern and tavern.owner_id and tavern.owner_id != user_id:
            from ...core.notifications import notify_new_visitor
            background_tasks.add_task(
                notify_new_visitor,
                space_id,
                tavern.name,
                tavern.owner_id,
                user_id or "访客",
            )

    return result


