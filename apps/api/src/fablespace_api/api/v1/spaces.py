@router.post("/{space_id}/visitor-notes")
def create_visitor_note(
    request: Request,
    background_tasks: BackgroundTasks,
    space_id: str,
    data: VisitorNoteCreateRequest,
) -> dict[str, Any]:
    user_id = get_user_id(request)
    result = spaces_service(request).create_visitor_note(
        space_id,
        data.to_payload(),
        user_id,
    )

    # Trigger notification for tavern owner
    tavern = spaces_service(request).store.get_space(space_id)
    if tavern and tavern.owner_id and tavern.owner_id != user_id:
        from ...core.notifications import notify_new_guest_message
        background_tasks.add_task(
            notify_new_guest_message,
            space_id,
            tavern.name,
            tavern.owner_id,
            data.visitor_nickname or "访客",
        )

    return result


@router.delete("/{space_id}/visitor-notes/{note_id}")
def delete_visitor_note(
    request: Request,
    space_id: str,
    note_id: str,
) -> dict[str, Any]:
    service = spaces_service(request)
    user_id = get_user_id(request)
    note_store = service.visitor_note_store
    note = note_store.get_note(space_id, note_id) if note_store else None
    if note and note.get("visitor_id") != user_id:
        require_session_capability(request, CREATOR_CAPABILITY)
    return service.delete_visitor_note(
        space_id,
        note_id,
        user_id,
    )
