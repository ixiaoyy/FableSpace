router = APIRouter(prefix="/spaces", tags=["spaces"])


@router.get("", response_model=SpaceListResponse)
def list_spaces(
    request: Request,
    lat: float | None = None,
    lon: float | None = None,
    radius: float = 5000,
    access: str | None = None,
    status: str | None = None,
    place_type: str = "",
    special_type: str = "",
    q: str = "",
    search: str = "",
    owner_id: str = "",
    limit: int | None = None,
    offset: int = 0,
) -> dict[str, Any]:
    query = q or search
    return spaces_service(request).list_spaces(
        lat=lat,
        lon=lon,
        radius=radius,
        access=access,
        status=status,
        place_type=place_type,
        special_type=special_type,
        query=query,
        owner_id=owner_id,
        limit=limit,
        offset=offset,
    )


